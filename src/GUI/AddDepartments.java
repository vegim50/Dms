    private void deleteDepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDepButtonActionPerformed
        // TODO add your handling code here:
         try{
            int row = tabela.getSelectedRow();
            if(row > -1){
                Object [] ob = {"Yes","No"};
                int i = javax.swing.JOptionPane.showOptionDialog(this, "Do you want to delete it?", "Delete:", JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
                if(i==0){
                    Departament dep = this.depTableModel.getDepartment(row);
                    depRepository.remove(dep);
                    tabelaLoad();
                    emptyObject();
                    JOptionPane.showMessageDialog(this, "Document has been deleted succesfully!");
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"You haven't selected nothing to delete!");
            }
        }catch(DepException pe){
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_deleteDepButtonActionPerformed

    public void emptyObject() {
       tabela.clearSelection();
        txtDepartament.setText("");
    }
    
    private void tabelaLoad() {
        List<Departament> lista = depRepository.findAll();
        depTableModel.add(lista);
        tabela.setModel(depTableModel);
        depTableModel.fireTableDataChanged();
        tabelaSelectedIndexChange();

    }
    
    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Departament dep = depTableModel.getDepartment(selectedIndex);

                    txtDepartament.setText(dep.getName()); 
                }
            }
        });
    }
    
    private void addDepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDepButtonActionPerformed
        // TODO add your handling code here:
         
        
         try {
            int row = tabela.getSelectedRow();
            if (txtDepartament.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please write department:", "Department:", JOptionPane.ERROR_MESSAGE);
            }else {
                if (row == -1) {
                    Departament dep = new Departament();
                     dep.setName(txtDepartament.getText());
                    depRepository.create(dep);
                    JOptionPane.showMessageDialog(this, "Department has been saved!");
                } else {
                    Departament dep = this.depTableModel.getDepartment(row);
                     dep.setName(txtDepartament.getText());
                    depRepository.edit(dep);
                    JOptionPane.showMessageDialog(this, "Department has been edited!");
                }
                emptyObject();
                tabelaLoad();
            };

        } catch (DepException pe) {
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
        
    }//GEN-LAST:event_addDepButtonActionPerformed


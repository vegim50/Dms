    private void txtDepartamentKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDepartamentKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(isAlphabetic(c) || c == KeyEvent.VK_BACKSPACE){
            txtDepartament.setEditable(true);
        }
        else{
            txtDepartament.setEditable(false);
        }
    }//GEN-LAST:event_txtDepartamentKeyPressed



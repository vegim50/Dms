    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        choosenFile = chooseFile();
        
        if(choosenFile == null)
        {
            return;
        }
        
        if(choosenFile.length() > 262144000)//250*1024*1024=262144000
        {
            chooser.cancelSelection();
            choosenFile = null;
           
            JOptionPane.showMessageDialog(this, "Files larger than 250MB are not allowed!");
            return;
        }
        
        if (! isAllowedFile(choosenFile))
        {
            chooser.cancelSelection();
            choosenFile = null;
            
            JOptionPane.showMessageDialog(this, "This file type is not allowed");
            return;
        }

        Path movefrom = Utils.getPathFrom(choosenFile);

        Utils.makeDirIfNotExists("C:\\DMS-Files\\");
        
        target = FileSystems.getDefault().getPath("C:\\DMS-Files\\" + Utils.getUniqueName(choosenFile.getName()));

        try{
            Files.copy(movefrom, target, StandardCopyOption.REPLACE_EXISTING);
        } catch (IOException ex) {
            Logger.getLogger(AddDocument.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
     private boolean isAllowedFile(File file) 
    {
        return allowedFileTypes.contains(Utils.getFileExtension(file));
    }
    
    private File chooseFile()
    {
        int returnVal = chooser.showOpenDialog(null);
        return chooser.getSelectedFile();
    }
    

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
    try{
        if (choosenFile == null) {
            JOptionPane.showMessageDialog(this, "Please select a File:", "File:", JOptionPane.ERROR_MESSAGE);
        }else if (target == null) {
            JOptionPane.showMessageDialog(this, "Please select a File:", "File:", JOptionPane.ERROR_MESSAGE);
        }else if (categoryComboBox.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please choose the Category:", "Category:", JOptionPane.ERROR_MESSAGE);
        }else if (txtDescription.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please choose the Description:", "Description:", JOptionPane.ERROR_MESSAGE);
        }else{
            if(!((choosenFile == null)||(target == null)||(categoryComboBox.getSelectedIndex() == -1)||(txtDescription.getText().equals(""))))
            {
                Data data = new Data();

                Category category = (Category) categoryComboBox.getSelectedItem();
                Departament departament = null;
                    try{
                    departament = depRepository.findById(usi.getDepartment());
                    }catch (DepException ex) {
                        Logger.getLogger(AddUsers.class.getName()).log(Level.SEVERE, null, ex);
                    }
                data.setCategory(category);

                data.setCreated(new Date());

                data.setOwner(usi);

                data.setDescription(txtDescription.getText());

                data.setStatus(null);

                data.setDepartment(departament);

                data.setIsDeleted(false);

                data.setPath(target.toString());

                data.setRealname(choosenFile.getName());

                JOptionPane.showMessageDialog(null, "Document is saved successfully." );

                dmsRepository.create(data);
            }
            emptyObject();
        }
    } catch (DmsException ex) {
        Logger.getLogger(AddDocument.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_submitButtonActionPerformed


